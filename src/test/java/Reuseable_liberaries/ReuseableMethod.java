package Reuseable_liberaries;
 public class ReuseableMethod {
    //create a void method to add two numbers
    public static void addTwoNumbers(int num1, int num2){
        int i = num1+num2;
        System.out.println("the result of two numbers is "+ i);
    }//end of addTwoNumbers Method

    //create a return integer method to subtract
    public static int subtractTwoNumbers(int a, int b){
        int i = a-b;
        System.out.println("the result of two numbers subtraction is " + i);
        return i;
    }//end of subtract two numbers
}//end of java class
